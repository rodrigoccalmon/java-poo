package exercicios;

public class CalculadoraMedia {

	public static void main(String[] args) {
		double nota1 = 8.0;
		double nota2 = 7.5;
		double nota3 = 5.5;
		double nota4 = 9.5;
		
		double media = (nota1+nota2+nota3+nota4) / 4;		
		
		System.out.println("A média das 4 notas foi: "+ media);
	}

}
